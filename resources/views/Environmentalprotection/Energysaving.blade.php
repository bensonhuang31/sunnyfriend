                    <p style="color:#009943">1.溫室氣體減量目標</br><span style="color:#555555">元長一廠計畫於2018年進行設備汰舊更新之工程，並新增熱交換系統，來充分的利用焚化系統的熱能，以減少燃料的使用量。並以含硫量較少的柴油來替代重油，最近一期係以2015年為基準年，預定於2019年完成改善後可達成總營運量減碳3%之目標。</span></p>
                    <p style="color:#009943">2.節電目標</br><span style="color:#555555">本公司進行中央空調管理、更換省電照明等措施，推動隨手關燈，並向經濟部認購綠色電力，最近一期係以2015年為基準年，預定於2017年可達成用電減量3%之目標。</span></p>
                    <p style="color:#009943">3.廢棄物管理目標</br><span style="color:#555555">為減少廢棄物的產生量，推行廢棄物分類，使可再利用的資源能再生使用，並推動文件影印時背面紙的重覆使用以減少用紙量，最近一期係以2015年為基準年，預定於2017年可達成廢棄物減量1%之目標。</span></p>
